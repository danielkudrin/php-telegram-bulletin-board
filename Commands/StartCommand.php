/**
 * Start command
 *
 * Gets executed when a user first starts using the bot.
 *
 * When using deep-linking, the parameter can be accessed by getting the command text.
 *
 * @see https://core.telegram.org/bots#deep-linking
 */

namespace Longman\TelegramBot\Commands\SystemCommands;

use Longman\TelegramBot\Commands\SystemCommand;
use Longman\TelegramBot\Entities\InlineKeyboard;
use Longman\TelegramBot\Entities\InlineKeyboardButton;
use Longman\TelegramBot\Entities\Keyboard;
use Longman\TelegramBot\Entities\ServerResponse;
use Longman\TelegramBot\Exception\TelegramException;

class StartCommand extends SystemCommand
{
    /**
     * @var string
     */
    protected $name = 'start';

    /**
     * @var string
     */
    protected $description = 'Показать проекты АнтифинРазведка';

    /**
     * @var string
     */
    protected $usage = '/start';

    /**
     * @var string
     */
    protected $version = '1.2.0';

    /**
     * @var bool
     */
    protected $private_only = true;

    /**
     * Main command execution
     *
     * @return ServerResponse
     * @throws TelegramException
     */
    public function execute(): ServerResponse
    {
         $keyboard = new InlineKeyboard(
            [
                new InlineKeyboardButton(['text' => 'Project 1', 'callback_data' => '/project-1']),
                new InlineKeyboardButton(['text' => 'Project 2', 'callback_data' => '/project-2']),
            ],
            [
                new InlineKeyboardButton(['text' => 'Project 3', 'callback_data' => '/project-3']),
            ],
        );

        return $this->replyToChat('Выберите услугу из предложенных', [
            'reply_markup' => $keyboard,
        ]);
    }
}
